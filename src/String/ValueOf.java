package String;

public class ValueOf {
    public static void main(String[] args) {
        int n = 345;
        float f = 199.98f;
        String str = "hello";
        boolean b = true;
        System.out.println(n);
        System.out.println(f);
        System.out.println(str);
        System.out.println(b);

    }
}
